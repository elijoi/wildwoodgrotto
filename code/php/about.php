                <h1>About</h1>
                <p>The OUTwright festival celebrates the LGBTQ contribution to theatre while also serving as an incubator for developing work by or about the LGBTQ community.</p>

				<p>OUTwright 2015 will take place for two weeks at The Funhouse Lounge (2432 SE 11th Ave. Portland, OR 97214) June 14-28.</p>

				<p>This year the festival's centerpiece is the world premier of a full production of "Under the Influence". "Under the Influence" has a book and lyrics by Ernie Lijoi - currently represented as a lyricist on the Broadway stage with "It Should Have Been You" (which has been nominated for a Outer Critics Circle Award for best score.)</p>

				<p>OUTwright is also excited to present a new musical workshop "Oso Fabuloso and the Bearbacks" by returning playwright J. Julian Christopher, as well as four other staged readings during the festival.</p>
